        try {
            $comment->save();
            return redirect()->route('showProduct','1')->with('success','Gửi bình luận thành công');
        } catch (\Exception $e) {
            Log::error($e->getMessage());
            return redirect()->route('showProduct','1')->with('error','Thêm bình luận không thành công');
        }

    }

